#suma y resta (+ y -)
suma= 12 + 5
resta= 12 - 5

#multiplicación y división (* y /)
multiplicación= 12 * 5
división= 12 / 5 #la división nos devuelve un dato float

#exponente (**)
exponente= 12 ** 5

#división baja (//)
división_baja= 12 // 5 #devuelve entero redondeado hacia abajo

#resto o modulo (%)
resto= 12 % 5

print(suma)
print(resta)
print(multiplicación)
print(división)
print(exponente)
print(división_baja)
print(resto)

